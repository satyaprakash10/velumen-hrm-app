/**
 * @param {object} opts
 * @param {string} [opts.module]
 * @param {string} [opts.action]
 * @param {string} [opts.type] success | error | info
 * @returns {string} inner HTML for SVG paths
 */
export function getToastIconPaths(opts = {}) {
  const action = (opts.action || "").toLowerCase();
  const mod = (opts.module || "").toLowerCase();

  if (action === "delete" || action === "remove") return ICONS.trash;
  if (action === "download") return ICONS.download;

  const key = MODULE_DEFAULT[mod] || MODULE_DEFAULT.general;
  if (opts.type === "error") return ICONS.alert;
  return ICONS[key] || ICONS.info;
}
